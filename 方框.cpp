{
	int i, j;
	int val;
	
	printf("请输入一个数值，以创造一个正方形空心方框："); 
	scanf("%d", &val);
	
	for (i=1; i<=val; i++)
	{
		printf("*");	
	}
	printf("\n");
	
	for (i=1; i<=val-2; i++)
	{
		printf("*");
		for (j=1; j<=val-2; j++)
		{
			printf(" ");
		}
		printf("*\n");
	}
	
	for (i=1; i<=val; i++)
	{
		printf("*");	
	}
	printf("\n");	
}
